export function DashboardSidebar() {
  const pathname = usePathname();
  const { collapsed, toggle } = useSidebar();
  const [role, setRole] = useState<Role | null>(null);
  useEffect(() => {
    try {
      const u = localStorage.getItem("user");
      if (u) {
        const user = JSON.parse(u);
        setRole(user.role as Role);
      }
    } catch {
      setRole(null);
    }
  }, []);
  const { t } = useI18n();
  const nav = role ? PORTAL_NAV[role] : [];
  return (
    <aside
      className={`fixed left-0 top-0 z-40 h-screen transition-all duration-300 ${collapsed ? "w-16" : "w-64"} border-r border-card-border bg-card`}
    >
      <div className="flex h-16 items-center justify-between px-4 border-b border-card-border">
        {!collapsed && (
          <Link href="/dashboard" className="text-xl font-bold text-gold">
            ProcurAI
          </Link>
        )}{" "}
        <button
          onClick={toggle}
          className="p-2 rounded-lg text-muted-foreground hover:text-foreground hover:bg-muted transition-colors"
        >
          <svg
            className="w-5 h-5"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
            strokeWidth={1.5}
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d={
                collapsed
                  ? "M13 5l7 7-7 7M5 5l7 7-7 7"
                  : "M11 19l-7-7 7-7m8 14l-7-7 7-7"
              }
            />
          </svg>
        </button>
      </div>
      <nav className="flex-1 overflow-y-auto p-3 space-y-1">
        {nav.map((item) => {
          const active =
            pathname === item.href ||
            (item.href !== "/dashboard" && pathname.startsWith(item.href));
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-all",
                active
                  ? "bg-gold/10 text-gold border border-gold/20"
                  : "text-muted-foreground hover:text-foreground hover:bg-muted border border-transparent",
              )}
              title={collapsed ? t(item.labelKey) : undefined}
            >
              <svg
                className="w-5 h-5 shrink-0"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
                strokeWidth={1.5}
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d={item.icon}
                />
              </svg>
              {!collapsed && <span>{t(item.labelKey)}</span>}{" "}
            </Link>
          );
        })}{" "}
      </nav>
      {!collapsed && (
        <div className="p-3 border-t border-card-border">
          <button
            onClick={() => {
              localStorage.clear();
              document.cookie.split(";").forEach(c => {
                document.cookie = c.trim().split("=")[0] + "=; expires=Thu, 01 Jan 1970 00:00:00 GMT; path=/";
              });
              window.location.href = "/login";
            }}
            className="flex w-full items-center gap-3 px-3 py-2.5 rounded-lg text-sm text-muted-foreground hover:text-destructive hover:bg-destructive/5 transition-all"
          >
            <svg
              className="w-5 h-5"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
              strokeWidth={1.5}
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1"
              />
            </svg>{" "}
            Sign out{" "}
          </button>
        </div>
      )}{" "}
    </aside>
  );
}
